function unique(arr) {
    let outArr = [];

    arr.forEach(elem => {
        if (!outArr.includes(elem)) {
            outArr.push(elem);
        }
    });

    return outArr;
}

let strings = ["кришна", "кришна", "харе", "харе",
    "харе", "харе", "кришна", "кришна", ":-O"
];

console.log(unique(strings)); // кришна, харе, :-O